# Check prerequisites
check_prerequisites() {
    print_header "Checking Prerequisites"
    
    # Check Terraform
    if ! command -v terraform &> /dev/null; then
        print_error "Terraform not found. Please install it first."
        exit 1
    fi
    print_success "Terraform installed"
    
    # Check if Terraform is initialized
    if [ ! -d "${TERRAFORM_DIR}/.terraform" ]; then
        print_error "Terraform not initialized. Run ./01-terraform-init.sh first."
        exit 1
    fi
    print_success "Terraform initialized"
    
    # Check AWS credentials
    if ! aws sts get-caller-identity &> /dev/null; then
        print_error "AWS credentials not configured."
        exit 1
    fi
    print_success "AWS credentials configured"
}

# Validate Terraform configuration
validate_terraform() {
    print_header "Validating Terraform Configuration"
    
    cd "$TERRAFORM_DIR"
    
    print_info "Running terraform validate..."
    if terraform validate; then
        print_success "Configuration is valid"
    else
        print_error "Configuration validation failed"
        exit 1
    fi
    
    cd "$SCRIPT_DIR"
}

# Create Terraform plan
create_plan() {
    print_header "Creating Terraform Execution Plan"
    
    cd "$TERRAFORM_DIR"
    
    print_info "Running terraform plan..."
    terraform plan -out=tfplan
    
    print_success "Execution plan created"
    
    cd "$SCRIPT_DIR"
}

# Apply Terraform plan
apply_plan() {
    print_header "Applying Terraform Plan"
    
    cd "$TERRAFORM_DIR"
    
    print_warning "This will create AWS resources that may incur costs."
    echo ""
    read -p "Do you want to continue? (yes/no): " CONFIRM
    
    if [ "$CONFIRM" != "yes" ]; then
        print_info "Terraform apply cancelled"
        rm -f tfplan
        exit 0
    fi
    
    print_info "Applying infrastructure changes..."
    terraform apply tfplan
    
    # Clean up plan file
    rm -f tfplan
    
    print_success "Infrastructure created successfully"
    
    cd "$SCRIPT_DIR"
}

# Export outputs to deployment-info.txt
export_outputs() {
    print_header "Exporting Terraform Outputs"
    
    cd "$TERRAFORM_DIR"
    
    print_info "Generating deployment-info.txt..."
    
    # Create deployment-info.txt header
    cat > "$DEPLOYMENT_INFO" << 'EOF'
#!/bin/bash
################################################################################
# Deployment Information
# Auto-generated by Terraform - DO NOT EDIT MANUALLY
# Source variables with: source deployment-info.txt
################################################################################

EOF
    
    # Export all Terraform outputs as environment variables
    echo "# General Configuration" >> "$DEPLOYMENT_INFO"
    echo "export REGION=\"$(terraform output -raw aws_region)\"" >> "$DEPLOYMENT_INFO"
    echo "export AWS_ACCOUNT_ID=\"$(terraform output -raw aws_account_id)\"" >> "$DEPLOYMENT_INFO"
    echo "export PROJECT_NAME=\"$(terraform output -raw project_name)\"" >> "$DEPLOYMENT_INFO"
    echo "" >> "$DEPLOYMENT_INFO"
    
    echo "# VPC Configuration" >> "$DEPLOYMENT_INFO"
    echo "export VPC_ID=\"$(terraform output -raw vpc_id)\"" >> "$DEPLOYMENT_INFO"
    echo "export PUBLIC_SUBNET_ID=\"$(terraform output -raw public_subnet_id)\"" >> "$DEPLOYMENT_INFO"
    echo "export SECURITY_GROUP_ID=\"$(terraform output -raw security_group_id)\"" >> "$DEPLOYMENT_INFO"
    echo "" >> "$DEPLOYMENT_INFO"
    
    echo "# IAM Configuration" >> "$DEPLOYMENT_INFO"
    echo "export IAM_ROLE_NAME=\"$(terraform output -raw iam_role_name)\"" >> "$DEPLOYMENT_INFO"
    echo "export IAM_INSTANCE_PROFILE_NAME=\"$(terraform output -raw iam_instance_profile_name)\"" >> "$DEPLOYMENT_INFO"
    echo "" >> "$DEPLOYMENT_INFO"
    
    echo "# EC2 Instances" >> "$DEPLOYMENT_INFO"
    echo "export MASTER_INSTANCE_ID=\"$(terraform output -raw master_instance_id)\"" >> "$DEPLOYMENT_INFO"
    echo "export MASTER_PUBLIC_IP=\"$(terraform output -raw master_public_ip)\"" >> "$DEPLOYMENT_INFO"
    echo "export MASTER_PRIVATE_IP=\"$(terraform output -raw master_private_ip)\"" >> "$DEPLOYMENT_INFO"
    echo "" >> "$DEPLOYMENT_INFO"
    echo "export WORKER1_INSTANCE_ID=\"$(terraform output -raw worker1_instance_id)\"" >> "$DEPLOYMENT_INFO"
    echo "export WORKER1_PUBLIC_IP=\"$(terraform output -raw worker1_public_ip)\"" >> "$DEPLOYMENT_INFO"
    echo "export WORKER1_PRIVATE_IP=\"$(terraform output -raw worker1_private_ip)\"" >> "$DEPLOYMENT_INFO"
    echo "" >> "$DEPLOYMENT_INFO"
    echo "export WORKER2_INSTANCE_ID=\"$(terraform output -raw worker2_instance_id)\"" >> "$DEPLOYMENT_INFO"
    echo "export WORKER2_PUBLIC_IP=\"$(terraform output -raw worker2_public_ip)\"" >> "$DEPLOYMENT_INFO"
    echo "export WORKER2_PRIVATE_IP=\"$(terraform output -raw worker2_private_ip)\"" >> "$DEPLOYMENT_INFO"
    echo "" >> "$DEPLOYMENT_INFO"
    
    echo "# SSH Configuration" >> "$DEPLOYMENT_INFO"
    echo "export KEY_NAME=\"$(terraform output -raw key_name)\"" >> "$DEPLOYMENT_INFO"
    echo "export KEY_FILE=\"$(terraform output -raw key_file)\"" >> "$DEPLOYMENT_INFO"
    echo "" >> "$DEPLOYMENT_INFO"
    
    echo "# ECR Configuration" >> "$DEPLOYMENT_INFO"
    echo "export ECR_REGISTRY=\"$(terraform output -raw ecr_registry_url)\"" >> "$DEPLOYMENT_INFO"
    echo "export ECR_UI_REPO=\"$(terraform output -raw ecr_ui_repo)\"" >> "$DEPLOYMENT_INFO"
    echo "export ECR_CATALOG_REPO=\"$(terraform output -raw ecr_catalog_repo)\"" >> "$DEPLOYMENT_INFO"
    echo "export ECR_CART_REPO=\"$(terraform output -raw ecr_cart_repo)\"" >> "$DEPLOYMENT_INFO"
    echo "export ECR_ORDERS_REPO=\"$(terraform output -raw ecr_orders_repo)\"" >> "$DEPLOYMENT_INFO"
    echo "export ECR_CHECKOUT_REPO=\"$(terraform output -raw ecr_checkout_repo)\"" >> "$DEPLOYMENT_INFO"
    echo "" >> "$DEPLOYMENT_INFO"
    
    echo "# DynamoDB Configuration" >> "$DEPLOYMENT_INFO"
    echo "export DYNAMODB_TABLE_NAME=\"$(terraform output -raw dynamodb_table_name)\"" >> "$DEPLOYMENT_INFO"
    echo "" >> "$DEPLOYMENT_INFO"
    
    print_success "deployment-info.txt created"
    
    # Make it executable
    chmod +x "$DEPLOYMENT_INFO"
    
    cd "$SCRIPT_DIR"
}

